public class If {
    public static void main(String[] args) {
        // if - условная конструкция
        int i = 0;
        int j = 1;
        if(i<j){
            System.out.println("Hello");
        }
        else System.out.println("Not Hello");
        // if (условние) { инструкция }
        // else {инструкция, если условие == false}
    }
}
